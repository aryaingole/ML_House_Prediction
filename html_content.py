def generate_html_report(input_features, predicted_price):
    """Generates a simple HTML string to display the prediction results."""

    # Format the price with commas and two decimal places
    formatted_price = f"${predicted_price:,.2f}"

    # Generate labels for display
    feature_labels = [
        "Median Income (in $10k)", 
        "Average Rooms", 
        "Latitude", 
        "Longitude"
    ]
    
    input_list = "".join([
        f"<li><span class='label'>{label}:</span> {value}</li>"
        for label, value in zip(feature_labels, input_features)
    ])

    html_template = f"""
    <!DOCTYPE html>
    <html lang="en">
    <head>
        <meta charset="UTF-8">
        <title>House Price Prediction Result</title>
        <style>
            body {{ font-family: Arial, sans-serif; margin: 50px; background-color: #f4f4f9; }}
            .container {{ background-color: #ffffff; padding: 30px; border-radius: 8px; box-shadow: 0 4px 8px rgba(0, 0, 0, 0.1); max-width: 600px; margin: auto; }}
            h1 {{ color: #333; border-bottom: 2px solid #eee; padding-bottom: 10px; }}
            h2 {{ color: #007bff; margin-top: 20px; }}
            .result {{ font-size: 2em; font-weight: bold; color: #28a745; margin: 15px 0; }}
            ul {{ list-style-type: none; padding: 0; }}
            li {{ margin-bottom: 10px; background-color: #e9ecef; padding: 8px; border-radius: 4px; }}
            .label {{ font-weight: bold; width: 250px; display: inline-block; }}
        </style>
    </head>
    <body>
        <div class="container">
            <h1>üè† California House Price Prediction</h1>
            
            <h2>Input Features Used:</h2>
            <ul>
                {input_list}
            </ul>

            <h2>Predicted Median House Price:</h2>
            <div class="result">{formatted_price}</div>
            
            <p style="font-size: 0.9em; color: #6c757d;">
                *Note: This prediction is generated by a simplified Linear Regression model 
                trained on the California Housing Dataset and should be used for educational purposes only.
            </p>
        </div>
    </body>
    </html>
    """
    return html_template

